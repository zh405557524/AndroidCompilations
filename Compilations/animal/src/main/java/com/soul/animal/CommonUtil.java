    /**
     * is null or its size is 0
     * <p/>
     * <pre>
     * isEmpty(null)   =   true;
     * isEmpty({})     =   true;
     * isEmpty({1})    =   false;
     * </pre>
     *
     * @param <V>
     * @param sourceList
     * @return if list is null or its size is 0, return true, else return false.
     */
    public static <V> boolean isEmpty(List<V> sourceList) {
        return (sourceList == null || sourceList.size() == 0);
    }

    public static boolean isEmpty(String content) {
        return TextUtils.isEmpty(content) || "null".equals(content);
    }

    public static String toString(Object objectValue) {
        String strValue = "";
        try {
            strValue = String.valueOf(objectValue);
        } catch (Exception e) {
            e.printStackTrace();
        }
        return strValue;
    }


    private static List<Long> countList = new ArrayList<>();

    /**
     * 指定时间内执行的次数
     *
     * @param count 指定次数
     * @param time  指定时间 单位 ms
     * @return true  time 内执行了 count 次
     */
    public static boolean isClickAvailable(int count, long time) {
        countList.add(System.currentTimeMillis());
        if (countList.size() > count) {
            countList.remove(0);
        }
        if (countList.size() == count) {
            if ((countList.get(count - 1) - countList.get(0)) <= time) {
                countList.clear();
                return true;
            }
        }
        return false;
    }

